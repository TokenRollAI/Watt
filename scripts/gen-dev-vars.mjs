#!/usr/bin/env node
/**
 * scripts/gen-dev-vars.mjs
 * 从项目根 .env 生成 packages/gateway/.dev.vars（wrangler dev 本地 secrets）。
 *
 * 白名单透传（fix 7）：只把 WORKER_VARS 列出的、Worker 运行时真正需要的变量写入
 * .dev.vars。随各 Phase 增长把新变量加进白名单；部署/管理凭据 CLOUDFLARE_*
 * **永不**进入白名单（那是 provision/deploy 的子进程 env，不是 Worker 运行时 secret）。
 *
 * Phase 0：gateway 零凭据需求，WORKER_VARS 为空数组 → 生成只含注释头的空 .dev.vars。
 *
 * .dev.vars 必须已被 .gitignore 忽略（见根 .gitignore）。本脚本不打印任何值。
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

// Worker 运行时白名单。随 Phase 增长在此登记真正需要注入 wrangler dev 的变量名。
// CLOUDFLARE_* 永不入列。
//
// Phase 1（Auth 内核）：
// - WATT_ADMIN_PRINCIPAL：种子引导初始 admin principal（§6.5c），Worker 运行时读取。
// - WATT_JWT_PRIVATE_JWK：平台 Ed25519 私钥 JWK JSON（签发/JWKS）。**理由**：Worker 运行时
//   需用它签发 token 并派生 JWKS 公钥；本地 wrangler dev 才用到。**注意**：这是运行时私钥，
//   本地开发应放一把**专用开发/测试密钥**（不与生产共用），且 .dev.vars 已被 .gitignore 忽略，
//   不会入库。若 .env 未提供该键（默认未提供），生成的 .dev.vars 就不含它——本地 vitest 集成测试
//   走的是 packages/gateway/test/fixtures 的专用测试密钥（经 vitest miniflare.bindings 注入），
//   不依赖 .dev.vars。生产私钥走 `wrangler secret put WATT_JWT_PRIVATE_JWK`（见 scripts/gen-jwt-keys.mjs）。
// Phase 6（飞书出站接线 R24）→ P1 飞书 plugin 化后移除：飞书凭据（FEISHU_APP_ID/SECRET/ENCRYPT_KEY/
//   VERIFICATION_TOKEN）随 P1 迁往 watt-plugin-feishu Worker 自持（其 .dev.vars / wrangler secret），
//   gateway 出站经通用分发器（event/plugin-sender.ts）§11.4 调 channel-adapter plugin——gateway 不再持有
//   任何飞书凭据。故 WORKER_VARS 不再含 FEISHU_*。
const WORKER_VARS = ['WATT_ADMIN_PRINCIPAL', 'WATT_JWT_PRIVATE_JWK'];

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');
const envPath = resolve(root, '.env');
const outPath = resolve(root, 'packages/gateway/.dev.vars');

let raw;
try {
  raw = readFileSync(envPath, 'utf8');
} catch {
  console.error(`gen-dev-vars: .env not found at ${envPath}`);
  process.exit(1);
}

const allow = new Set(WORKER_VARS);
const lines = [];
const names = [];
for (const line of raw.split('\n')) {
  const trimmed = line.replace(/\r$/, '').trim(); // 处理 CRLF
  if (!trimmed || trimmed.startsWith('#')) continue;
  const eq = trimmed.indexOf('=');
  if (eq <= 0) continue;
  const key = trimmed.slice(0, eq).trim();
  const value = trimmed.slice(eq + 1).replace(/\r$/, '');
  if (!/^[A-Za-z_][A-Za-z0-9_]*$/.test(key)) continue;
  if (!allow.has(key)) continue; // 白名单外一律不透传
  lines.push(`${key}=${value}`);
  names.push(key);
}

const header = '# GENERATED by scripts/gen-dev-vars.mjs from .env (whitelist) — do not commit.\n';
const body = lines.length ? `${lines.join('\n')}\n` : '';
writeFileSync(outPath, `${header}${body}`, 'utf8');
console.log(`gen-dev-vars: wrote ${names.length} whitelisted var(s) to ${outPath}`);
console.log(`gen-dev-vars: keys: ${names.length ? names.join(', ') : '(none — Phase 0)'}`);
